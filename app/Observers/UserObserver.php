     * Handle the user "created" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function created(User $user)
    {
        $token = new Token();
        $token->code = strtoupper(uniqid());
        $token->user_id = $user->id;
        $token->save();
        $wallet = new Wallet();
        $wallet->charge = env('GIFT');
        $wallet->user_id = $user->id;
        $wallet->save();
// TODO Send gift charge email to user
    }

